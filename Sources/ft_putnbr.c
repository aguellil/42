#include <unistd.h>

void	ft_putnbr(int n)
{
	char	d;

	if (n < 0)
	{
		write(1, "-", 1);
		ft_putnbr(-n);
		return ;
	}
	d = '0' + n % 10;
	if (n / 10 != 0)
		ft_putnbr(n / 10);
	write(1, &d, 1);
	return ;
}
