#include "libft.h"

/*
** 1. Implementation
*/

/*
** 1.1 Iterative
*/

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;

	i = 0;
	while (s[i])
	{
		(*f)(i, s + i);
		i++;
	}
}
